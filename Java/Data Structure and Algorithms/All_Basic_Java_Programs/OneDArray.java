package All_Basic_Java_Programs;

import java.util.Scanner;

public class OneDArray {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        scan.close();
        int arr[] = new int[5];
        arr[0] = 30;
        arr[1] = 20;
        arr[2] = 90;
        arr[3] = 32;
        arr[4] = 80;
        System.out.println("The first element of an array: " + arr[0]);
        System.out.println("The second element of an array: " + arr[1]);
        System.out.println("The third element of an array: " + arr[2]);
        System.out.println("The fourth element of an array: " + arr[3]);
        System.out.println("The fifth element of an array: " + arr[4]);


    }
}
